# Operadores de comparación
# Igualdad
print(10 == 10) # True

# Diferencia
print(10 != 10) # False

# Mayor que
print(10 > 5) # True

# Menor que
print(10 < 5) # False

# Mayor o igual que
print(10 >= 10) # True

# Menor o igual que
print(10 <= 5) # False


# Operadores lógicos
# AND
print(True and True) # True

# OR
print(True or False) # True

# NOT
print(not True) # False